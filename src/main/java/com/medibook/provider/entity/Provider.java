//**Why this entity is designed this way:**
//```
//Two table design (users + providers):
//→ keeps user account separate from professional profile
//→ if doctor deactivates account → user record deleted
//  but provider history stays
//→ clean separation of concerns
//
//isVerified = false by default:
//→ doctor cannot appear in search until admin approves
//→ prevents fake doctors from joining platform
//→ this is a strict PDF requirement
//
//avgRating updated automatically:
//→ every time patient submits review
//→ ReviewService calls ProviderService.updateRating()
//→ recalculates average and updates this field